package Tricky_interviewquest;

public class Quiz_static {
	public static void main(String[] args) {
		String fruit = "Apple";
		switch (fruit) {
		case "Banana":
			System.out.println("banana");
			
		case "Apple":
			System.out.println("Apple");
            break;
		case "Orange":
			System.out.println("Orange");
			break;
		default:
			System.out.println("No match");
		}
	}
}
